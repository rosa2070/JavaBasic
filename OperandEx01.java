public class OperandEx01 {
    public static void main(String[] args) {
        // + - * 

        // 몫 - 소수점이하 실수
        // 몫 - 정수
        System.out.println(1 / 3);
        System.out.println(2 / 3);
        System.out.println(5 / 3);

        System.out.println(5 / 3.);

        // 나머지
        System.out.println(1 % 3);
        System.out.println(2 % 3);

        // 산술 연산
        // integer 이상 / long / float / double
        // byte, short, char => integer

    }
}