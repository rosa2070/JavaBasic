package back1;
public class HelloWorld {
    public static void main(String[] args) {
        // 프로그램 시작

        // 연결해서 출력
        System.out.print("Hello");
        System.out.print("안녕");

        System.out.println("Hello");
        System.out.println("안녕");
        
        // %n \n - 엔터키, \t 탭키
        System.out.printf("[%s]%n", "Hello");
        System.out.printf("[%s %s %s]", "Hello", "World", "안녕"); 

        // 프로그램 끝
    }
}